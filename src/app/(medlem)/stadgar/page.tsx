export default function Stadgar() {
  return (
    <section className="flex flex-col max-w-xl mx-auto p-3 prose">
      <article>
        <h2 className="text-2xl font-bold">
          Stadgar för Holmängens koloniförening i Vänersborg
        </h2>
        <h3>Föreningens namn</h3>
        <h4>§ 1</h4>
        <p>Föreningens namn är Holmängens Koloniförening.</p>

        <h3>Föreningen har till ändamål</h3>
        <h4>§ 2</h4>
        <p>
          Att av Vänersborgs kommun arrendera mark avsedd för
          koloniträdgårdsverksamhet.
        </p>
        <p>
          Att främja medlemmarnas intressen som trädgårdsodlare och att för
          föreningen och utomstående upprätthålla en god ordning inom området.
        </p>
        <p>
          Att ansvara för skötsel och underhåll av anläggningen och de
          tillgångar som finns på området och som är gemensamt för medlemmarna.
        </p>
        <p>Att stärka samhörigheten mellan föreningens medlemmar.</p>

        <h4>§ 3</h4>
        <p>
          Föreningen är ansluten till Koloniträdgårdsförbundet med dess region
          för Västra Sverige och har skyldighet att följa dess stadgar och
          arbeta i enlighet med dess målsättning.
        </p>

        <h4>§ 4</h4>
        <p>
          Föreningen har skyldighet att se till att hälsovårdsstadgan följs
          beträffande latrin- och sophämtning samt renhållning inom området.
        </p>

        <h3>Medlemmar</h3>
        <h4>§ 5</h4>
        <p>
          Till medlem i föreningen kan antagas myndig person som är intresserad
          av koloniträdgårdsverksamhet och innehar fast bostad i Vänersborgs
          kommun och inte äger nyttjanderätt till annan lott i koloniträdgård i
          Vänersborg. Medlem förbinder sig att inte upplåta koloniträdgårdslott
          till person som inte är medlem av föreningen samt vid
          nyttjanderättskontraktets upprättande inte har fast bostad i
          Vänersborgs kommun.
        </p>

        <h4>§ 6</h4>
        <p>
          Ansökan om inträde i föreningen prövas av styrelsen och ska göras
          skriftligen på föreningens formulär. Nyttjanderätt till kolonilott
          regleras genom särskilt avtal mellan medlem och Holmängens
          Koloniförening.
        </p>

        <h4>§ 7</h4>
        <p>
          Om medlem bryter mot bestämmelserna i stadgarna, i ordningsreglerna
          samt i nyttjanderättsavtalet, kan nyttjanderätten till
          koloniträdgårdslotten förklaras förverkad.
        </p>

        <h4>§ 8</h4>
        <p>
          Om nyttjanderätten till kolonilott övergår till annan person genom
          bodelning, arv eller testamente eller genom exekutiv försäljning eller
          i medlems konkurs är den nye innehavaren berättigad att inträda i
          föreningen om förutsättningarna i § 5 är uppfyllda. Ansökan sker i
          vanlig ordning och ska godkännas av styrelsen.
        </p>

        <h4>§ 9</h4>
        <p>
          Om nyttjanderätten i annat fall än som avses i § 8 övergår till annan
          person är den nye innehavaren berättigad att inträda som medlem i
          föreningen om förutsättningarna enligt § 5 och 6 är uppfyllda. Ansökan
          sker i vanlig ordning och ska godkännas av styrelsen. Den som övertar
          nyttjanderätt och förvärvar medlemskap i föreningen ska betala
          föreskriven inträdesavgift (se § 14).
        </p>

        <h4>§ 10</h4>
        <p>
          Medlem som upphör att vara nyttjanderättshavare ska anses ha utträtt
          ur föreningen när nyttjanderätten upphörde om inte styrelsen medger
          att medlemmen (om denne så önskar) får kvarstå som medlem.
        </p>

        <h4>§ 11</h4>
        <p>Styrelsen får utesluta medlem ur föreningen</p>
        <ol>
          <li>
            om medlem inte inom bestämd tid erlägger fastställda avgifter.
          </li>
          <li>
            om medlem bryter mot de bestämmelser som finns antagna i för området
            gällande arrendekontrakt mellan Vänersborgs kommun och Holmängens
            Koloniförening.
          </li>
          <li>
            om medlem bryter mot de bestämmelser som finns antagna i avtalet om
            nyttjanderätt mellan medlem och förening.
          </li>
          <li>
            om medlem inte i övrigt följer dessa stadgar eller behörigen
            utfärdade ordningsregler.
          </li>
          <li>
            om medlem verkar emot föreningens intressen och därmed skadar
            föreningens anseende.
          </li>
        </ol>
        <p>
          Föreligger anledning till medlems uteslutning ur föreningen ska
          medlemmen skriftligen varnas av styrelsen. Om medlemmen inte rättar
          sig senast 14 dagar efter att hen fått del av varningen eller inkommit
          med en godtagbar förklaring äger styrelsen rätt att utesluta
          medlemmen. Utesluten medlem äger inte rätt att återfå sin erlagda
          insats eller andra till föreningen erlagda avgifter och äger heller
          inte rätt till andel i föreningens tillgångar.
        </p>

        <h4>§ 12</h4>
        <p>
          Medlem får säga upp sig till utträde ur föreningen och anmälan om
          utträdet ska alltid ske skriftligen.
        </p>

        <h4>§ 13</h4>
        <p>
          Har medlem uteslutits eller på egen begäran utträtt ur föreningen får
          styrelsen säga upp medlemmens nyttjanderätt.
        </p>

        <h3>Avgifter</h3>
        <h4>§ 14</h4>
        <p>
          Då en medlem inträder i föreningen ska medlemmen betala en
          inträdesavgift vars storlek beslutas av årsmötet. Denna avgift
          återbetalas inte vid medlems utträde ur föreningen.
        </p>

        <h4>§ 15</h4>
        <p>
          Årsavgiften fastställs av ordinarie årsmöte. Avgiften ska vara
          inbetald till Holmängens Koloniförening av tomträttsinnehavaren senast
          den 31 mars det år avgiften avser.
        </p>

        <h4>§ 16</h4>
        <p>
          Om annan avgift eller extra debitering behövs för föreningens
          verksamhet får sådan beslutas av årsmötet eller ett extra
          föreningsmöte.
        </p>

        <h3>Styrelsen</h3>
        <h4>§ 17</h4>
        <p>
          En styrelse med minst tre och högst sju ordinarie ledamöter väljs på
          ordinarie årsmöte. Av styrelseledamöterna ska ordförande och kassör
          väljas särskilt. Styrelseledamot ska vara medlem i föreningen. Även
          medlems maka/make eller sambo får väljas till styrelseledamot. Dock
          får inte båda samtidigt inneha förtroendeuppdrag. Styrelseledamöter
          väljs för en period av två år och mandatperioden justeras så att max
          halva antalet medlemmar i styrelsen avgår vid ordinarie årsmöte.
          Ordförande och kassör avgår växelvis. Styrelsen har sitt säte i
          Vänersborg.
        </p>

        <h4>§ 18</h4>
        <p>
          Styrelsen konstituerar sig vid ett sammanträde som hålls senast 14
          dagar efter ordinarie årsmöte. Vid detta möte utses firmatecknare och
          sekreterare.
        </p>

        <h4>§ 19</h4>
        <p>
          Protokoll från styrelsemöte ska innehålla uppgifter om namn på
          närvarande ledamöter samt ärenden som behandlats och beslutats.
          Protokollet justeras av ordförande och en ledamot.
        </p>

        <h4>§ 20</h4>
        <p>
          De försäkringar som är gemensamma för föreningen tecknas av styrelsen.
        </p>

        <h4>§ 21</h4>
        <p>
          Styrelsen svarar för att arbeten på föreningens anläggning blir
          utförda. Arbeten på gemensamma områden och byggnader ska solidariskt
          utföras av medlemmarna och ingen får utebli från sådant arbete utan
          skäligt hinder. Material som inköps för dessa arbeten betalas
          gemensamt. Det är styrelsens ansvar att se till att drift och
          underhåll av gemensamma anläggningar och tillgångar sköts ekonomiskt
          på bästa sätt samt att fondera medel för reparationer och ny eller
          återanskaffning.
        </p>

        <h4>§ 22</h4>
        <p>
          Styrelsen är skyldig att övervaka att bestämmelserna i
          nyttjanderättsavtal och stadgar samt andra fattade beslut följs.
        </p>

        <h4>§ 23</h4>
        <p>De enskilda styrelseledamöterna har följande skyldigheter:</p>
        <ul>
          <li>
            Ordförande sammankallar styrelsen, upprättar dagordning och leder
            förhandlingarna.
          </li>
          <li>
            Sekreterare för protokoll vid förhandlingarna. Sekreteraren ansvarar
            även för att expediera skrivelser och protokoll samt omhänderta
            föreningens arkiv.
          </li>
          <li>
            Kassör ansvarar för att föra böcker över de ekonomiska transaktioner
            föreningen gör och att hålla dessa tillgängliga närhelst styrelse
            eller revisorer begär detta.
          </li>
        </ul>

        <h4>§ 24</h4>
        <p>
          Styrelseledamot är fri från ansvar för beslut som fattats av styrelsen
          då ledamoten ej varit närvarande. Fri från ansvar är även den som
          reserverat sig mot beslut.
        </p>

        <h4>§ 25</h4>
        <p>
          Styrelsen är beslutsför när minst hälften av ledamöterna är närvarande
          och beslut är utan reservationer. Vid lika röstetal har ordförande
          utslagsröst.
        </p>

        <h3>Revisorer</h3>
        <h4>§ 26</h4>
        <p>
          Räkenskaper ska fortlöpande granskas av två revisorer. De väljs av
          årsmötet och har en mandatperiod av två år samt avgår växelvis.
        </p>

        <h4>§ 27</h4>
        <p>
          Revisorerna ska årligen och senast två veckor före årsmötet avge
          revisionsberättelse. I denna ska bland annat anges om ansvarsfrihet
          för styrelsen tillstyrkes eller avstyrkes.
        </p>

        <h3>Räkenskaper</h3>
        <h4>§ 28</h4>
        <p>
          Föreningens räkenskapsår omfattar tiden för kalenderår. Av styrelsen
          upprättade balans-, vinst- och förlusträkningar jämte av styrelsen
          avgiven förvaltningsberättelse för räkenskapsåret skall vara
          revisorerna tillhanda senast den 1 februari.
        </p>

        <h3>Årsmöte</h3>
        <h4>§ 29</h4>
        <p>
          Medlemmarnas möjlighet att delta i beslutsfattandet för föreningen kan
          utövas på ordinarie årsmöte eller vid extra föreningsmöte. Vid dessa
          tillfällen har medlem en röst per kolonilott. Medlem får uppdra åt
          annan myndig person i sitt hushåll att utöva rösträtten eller ge
          fullmakt åt annan medlem som ombud. Inget ombud får företräda mer än
          en medlem. Fullmakten ska vara skriftlig. Ordinarie årsmöte ska hållas
          varje år före utgången av mars. Styrelsen kan när så krävs, kalla till
          extra föreningsmöte. Revisorerna får, om deras granskning föranleder
          detta, uppmana styrelsen att utlysa extra föreningsmöte som ska hållas
          så snart som möjligt med iakttagande av föreskriven kallelsetid. Extra
          föreningsmöte ska även utlysas om minst hälften av medlemmarna begär
          detta. Begäran om extra möte ska sändas skriftligt till styrelsen med
          uppgivande av sakskäl. Kallelse till ordinarie årsmöte ska ske minst
          14 dagar i förväg och kallelse till extra föreningsmöte skall ske
          senast 8 dagar i förväg. Kallelse och övriga meddelanden till medlem
          sändes per e-post (där så inte är möjligt sändes det per brev) eller
          anslås under tiden 15 april till 1 oktober inom koloniområdet.
        </p>

        <h4>§ 30</h4>
        <p>Vid ordinarie årsmöte skall följande finnas på dagordningen:</p>
        <ol>
          <li>Årsmötets öppnande</li>
          <li>Mötets behöriga utlysande</li>
          <li>Fastställelse av dagordning</li>
          <li>Fastställande av röstlängd</li>
          <li>Val av ordförande för årsmötet</li>
          <li>Val av sekreterare för årsmötet</li>
          <li>Val av två justeringsmän tillika rösträknare</li>
          <li>Styrelsens verksamhetsberättelse</li>
          <li>Revisorernas berättelse</li>
          <li>Fastställande av ekonomisk redovisning</li>
          <li>Beslut med ledning av den ekonomiska redovisningen</li>
          <li>Förslag till budget</li>
          <li>Fastställande av årsavgift och övriga avgifter</li>
          <li>Fråga om ansvarsfrihet för styrelsen</li>
          <li>Val av styrelse och revisorer samt val av ombud</li>
          <li>Val av valberedning</li>
          <li>Motioner</li>
          <li>Övriga ärenden</li>
          <li>Årsmötet avslutas</li>
        </ol>
        <p>
          Vid årsmötet får inga andra ärenden tas upp till avgörande än de som
          angivits i kallelsen. Medlemmar får ta del av motioner i samband med
          kallelsen. Protokollet från årsmötet sätts i en pärm i boden på
          koloniområdet efter det att justering är gjord.
        </p>

        <h4>§ 31</h4>
        <p>
          Motionsrätt har styrelse, revisorer och enskilda medlemmar. Motioner
          till årsmötet ska skriftligen insändas till styrelsen senast den 15
          januari. Över inkomna motioner och förslag ska styrelsen ta ställning.
          De redovisar sedan detta för årsmötet.
        </p>

        <h4>§ 32</h4>
        <p>
          Alla val och övriga frågor vid årsmötet avgörs genom enkel majoritet
          och öppen omröstning såvida inte årsmötet bestämmer något annat. Vid
          lika röstetal har ordförande utslagsröst. Vid val skall dock lottning
          fälla avgörandet vid lika röstetal.
        </p>

        <h3>Föreningens ekonomi</h3>
        <h4>§ 33</h4>
        <p>
          Under räkenskapsår uppkommet överskott ska efter täckning av tidigare
          balanserade underskott, tillföras föreningens kapital.
        </p>

        <h4>§ 34</h4>
        <p>
          Beslut om upplösning av föreningen ska tillstyrkas av minst ¾ av
          medlemmarna och fattas på två av varandra följande föreningsmöten
          (varav det ena ska vara ordinarie årsmöte). Vid föreningens upplösning
          ska de behållna tillgångarna användas till att gynna
          koloniträdgårdsrörelsen i Sverige.
        </p>

        <h4>§ 35</h4>
        <p>
          För föreningens förbindelser svarar endast föreningens tillgångar,
          däri inräknade förfallna, men inte betalda insatser och avgifter.
        </p>

        <h4>§ 36</h4>
        <p>
          Beslut om ändring av föreningens stadgar ska fattas på årsmötet.
          Ovanstående stadgar är antagna vid årsmötet 2020.
        </p>
      </article>
    </section>
  );
}
